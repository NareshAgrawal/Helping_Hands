    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
         int selectedRow = workRequestJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        
         NGO_ManagertoFieldWorker_WorkRequest request = (NGO_ManagertoFieldWorker_WorkRequest)workRequestJTable.getValueAt(selectedRow, 2);
        
        if(request.getStatus().equalsIgnoreCase("sent")){
            request.setReceiver(userAccount);
            request.setStatus("Pending");
            //request.setTempStatus("Pending");
            dB4OUtil.storeSystem(system);
            populateTable();
          //  btnRequest.setEnabled(true);
        }
         else
        {
            JOptionPane.showMessageDialog(null, "Please select a new request to Assign");
            
        } 
    }//GEN-LAST:event_btnAssignActionPerformed

