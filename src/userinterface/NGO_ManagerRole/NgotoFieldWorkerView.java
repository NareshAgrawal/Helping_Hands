     public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : ngoOrganization.getWorkQueue().getWorkRequestList()){
            if(request.getRole().equalsIgnoreCase("FieldWorker")){
            Object[] row = new Object[8];
            row[0] = request.getSender().getEmployee().getName();
            row[1] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[2] = request;
            row[3] = request.getRequestDate();
            row[4] = request.getResolveDate();
            row[5] = request.getStatus();
            row[6] = ((NGO_ManagertoFieldWorker_WorkRequest)request).getID();
            row[7] = ((NGO_ManagertoFieldWorker_WorkRequest)request).getName();
            model.addRow(row);
            }
        }
    }
    

