    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
       if((txtEducationLevel.getText().isEmpty())||(txtSkills.getText().isEmpty())){
            JOptionPane.showMessageDialog(null, "Fields can't be empty");
            return;
       }else{
           if(isStringValid(txtComment.getText()) == false||isStringValid(txtEducationLevel.getText()) == false||isStringValid(txtSkills.getText()) == false)    
        {
           
            JOptionPane.showMessageDialog(null, "Please enter a correct field ");
            return;
        }  
       String educationLevel = txtEducationLevel.getText();
       String skill =txtSkills.getText();
       Date date = new Date(); 
       
       RecruiterWorkRequest request = new RecruiterWorkRequest();
       request.setSender(userAccount);
       request.setStatus("Sent");
       request.setRequestDate(date);
       request.setRole("Recruiter");
       request.setEducation(educationLevel);
       request.setSkill(skill);
       request.setMessage(txtComment.getText());
       
       
        dB4OUtil.storeSystem(system);
        Organization org = null;
          for (Network network: system.getNetworkList()){

                    for (Enterprise enterprisetemp: network.getEnterpriseDirectory().getEnterpriseList()){

                        for (Organization organization : enterprisetemp.getOrganizationDirectory().getOrganizationList()){
                            if (organization instanceof NGOOrganization){
                                org = organization;
                                break;
                            }
                        }}}
                        if (org!=null){
                            org.getWorkQueue().getWorkRequestList().add(request);
                            userAccount.getWorkQueue().getWorkRequestList().add(request);
                            JOptionPane.showMessageDialog(null, "Requested" );
                            populateTable();
                            txtComment.setText("");
                            txtEducationLevel.setText("");
                            txtSkills.setText("");
                             
                             dB4OUtil.storeSystem(system);
                        }
       }
    }//GEN-LAST:event_btnRequestActionPerformed

