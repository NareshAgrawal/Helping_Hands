    public void populateTable(){
       
         DefaultTableModel model = (DefaultTableModel) recruiterJTable.getModel();
           model.setRowCount(0);
       
            for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[7];
            row[0] = request.getSender().getEmployee().getName();
            row[1] = request.getReceiver()==null?null:request.getReceiver().getEmployee().getName();;
            row[2] = request.getStatus();
            row[3] = request.getRequestDate();
            row[4] = request.getResolveDate();
          
            row[5] = ((RecruiterWorkRequest)request).getID();
            row[6] = ((RecruiterWorkRequest) request).getName();
            
            model.addRow(row);
        }  
        
        
    }
